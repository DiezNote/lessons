class Address {

    private int Index;
    private String Country;
    private String City;
    private String Street;
    private String House;
    private int Apartment;

    int getIndex() {
        return Index;
    }

    void setIndex(int var) {
        Index = var;
    }

    String getCountry() {
        return Country;
    }

    void setCountry(String var) {
        Country = var;
    }

    String getCity() {
        return City;
    }

    void setCity(String var) {
        City = var;
    }

    String getStreet() {
        return Street;
    }

    void setStreet(String var) {
        Street = var;
    }

    String getHouse() {
        return House;
    }

    void setHouse(String var) {
        House = var;
    }

    int getApartment() {
        return Apartment;
    }

    void setApartment(int var) {
        Apartment = var;
    }
}
